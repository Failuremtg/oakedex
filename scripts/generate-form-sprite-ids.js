function slotKey(dexId, form) {
  return `${dexId}-${form}`;
}

/** Derive PokeAPI name from (baseDexId, form). Returns null if no rule. */
function toPokeApiName(baseDexId, form, idToName) {
  const base = idToName[baseDexId];
  if (!base) return null;
  // Unown: API name is literally "unown-a", "unown-b", etc.
  if (form.startsWith('unown-')) return form;
  // Tauros Paldean (our form: combat/blaze/aqua → API: tauros-paldea-X-breed)
  if (baseDexId === 128 && ['combat', 'blaze', 'aqua'].includes(form))
    return `tauros-paldea-${form}-breed`;
  return `${base}-${form}`;
}

async function main() {
  const res = await fetch('https://pokeapi.co/api/v2/pokemon?limit=2000');
  const data = await res.json();
  const nameToId = {};
  const idToName = {};
  for (const r of data.results || []) {
    const id = parseInt(r.url.replace(/\/$/, '').split('/').pop(), 10);
    if (!isNaN(id)) {
      nameToId[r.name] = id;
      idToName[id] = r.name;
    }
  }

  const out = {};
  let matched = 0;
  for (const [baseDexId, form] of EXTRA_ENTRIES) {
    const key = slotKey(baseDexId, form);
    const apiName = toPokeApiName(baseDexId, form, idToName);
    const id = apiName ? nameToId[apiName] : null;
    if (id != null) {
      out[key] = id;
      matched++;
    }
  }

  const lines = [
    '/**',
    ' * PokeAPI pokemon IDs for form-specific official artwork.',
    ' * Generated by scripts/generate-form-sprite-ids.js from PokeAPI.',
    ' * Used when a Pokémon has a form (Mega, Unown, regional, etc.) so the correct sprite is shown.',
    ' */',
    '',
    'export const FORM_SPRITE_IDS: Record<string, number> = {',
    ...Object.entries(out)
      .sort((a, b) => a[0].localeCompare(b[0], undefined, { numeric: true }))
      .map(([k, v]) => `  '${k}': ${v},`),
    '};',
    '',
  ].join('\n');

  fs.writeFileSync(OUT_PATH, lines, 'utf8');
  console.log(`Wrote ${OUT_PATH}: ${matched}/${EXTRA_ENTRIES.length} form sprites mapped.`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
